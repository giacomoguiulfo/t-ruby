        compiler = TRuby::Compiler.new
        expect { compiler.compile_string(example.code) }.not_to raise_error
      end
    end

    describe "예제 3: 함수 타입" do
      let(:example) { examples.find { |e| e.trb? && e.code.include?("def add(a: Integer, b: Integer)") } }

